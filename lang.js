// دالة تغيير اللغة
function changeLanguage(lang) {
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'SELECT') {
                // معالجة خيارات select
                if (element.id === 'departmentSelect') {
                    const options = element.querySelectorAll('option');
                    options[0].textContent = translations[lang]['join.department'];
                    options[1].textContent = translations[lang]['departments.marketing.title'];
                    options[2].textContent = translations[lang]['departments.research.title'];
                    options[3].textContent = translations[lang]['departments.planning.title'];
                    options[4].textContent = translations[lang]['departments.design.title'];
                }
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    
    localStorage.setItem('preferred-language', lang);
}

// عند تحميل الصفحة
document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('preferred-language') || 'ar';
    changeLanguage(savedLang);
    
    document.querySelectorAll('.lang-btn').forEach(button => {
        button.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            changeLanguage(lang);
        });
    });
    
    // تحديث حقل سبب الانضمام
    const reasonField = document.getElementById('reason');
    if (reasonField) {
        reasonField.placeholder = translations[savedLang]['join.reason'];
    }
});